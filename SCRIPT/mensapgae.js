function display(data) {

    document.querySelector("#prod").textContent = null;
    data.forEach(function (el) {


        let card = document.createElement("div");

        let img = document.createElement("img");
        img.setAttribute("src", el.avatar)

        let title = document.createElement("h2");
        title.innerText = el.title;

        let catagory = document.createElement("h3");
        catagory.innerText = el.catagory;

        let price = document.createElement("h4");
        price.innerText = el.price;

        let discripcion = document.createElement("p");
        discripcion.innerText = el.Desrciption;

        let btn = document.createElement("button");
        btn.innerText = "Add To Bag";


        btn.addEventListener("click", function () {
            let favdata = JSON.parse(localStorage.getItem("favourites")) || [];

            let isalreadyincard = false;


            for (let j = 0; j < favdata.length; j++) {

                if (favdata[j].id === el.id) {
                    isalreadyincard = true;
                    break;
                }
            }


            if (isalreadyincard == true) {
       
                Swal.fire(
                    "Products already in  bag",
                    '',
                    'info'
                  )
                
            } else {
                favdata.push({ ...el, quantity: 1 });

                localStorage.setItem("favourites", JSON.stringify(favdata));
    
                Swal.fire(
                    "Products added to  bag",
                    '',
                    'success'
                  )
            }
        })

        card.append(img, title, catagory, price, discripcion, btn)

        document.querySelector("#prod").append(card);



    })
}

